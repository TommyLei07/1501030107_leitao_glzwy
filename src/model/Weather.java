package model;

public class Weather {
	public int id;
	public String Wdate;
	public String Wwea;
	public String Wtem;
	public String Wteml;
	public String Wtemh;
	public String Wwin;
	public String Wair;
	
	public int getId(){
		return id;
	}
	public void setId(int id){
		this.id=id;
	}
    public String getWdate() {
        return Wdate;
    }
    public void setWdate(String Wdate) {
        this.Wdate = Wdate;
    }
    public String getWwea() {
        return Wwea;
    }
    public void setWwea(String Wwea) {
        this.Wwea = Wwea;
    }
    
    public String getWtem() {
        return Wtem;
    }
    public void setWtem(String Wtem) {
        this.Wtem = Wtem;
    }
    
    public String getWteml() {
        return Wteml;
    }
    public void setWteml(String Wteml) {
        this.Wteml = Wteml;
    }
    public String getWtemh() {
        return Wtemh;
    }
    public void setWtemh(String Wtemh) {
        this.Wtemh = Wtemh;
    }
    public String getWwin() {
        return Wwin;
    }
    public void setWwin(String Wwin) {
        this.Wwin = Wwin;
    }
    public String getWair() {
        return Wair;
    }
    public void setWair(String Wair) {
        this.Wair = Wair;
    }
  
}
